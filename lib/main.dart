                        ],
                      ),
                      child: Icon(
                        Icons.lock_outline,
                        size: 50,
                        color: Color(0xFF667eea),
                      ),
                    ),
                    SizedBox(height: 24),
                    Text(
                      'Chào mừng trở lại!',
                      style: TextStyle(
                        fontSize: 28,
                        fontWeight: FontWeight.bold,
